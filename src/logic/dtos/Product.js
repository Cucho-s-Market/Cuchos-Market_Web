export class Product {
    constructor(sku, name, description, price, images, entryDate, brand, category) {
        this.sku = sku;
        this.name = name;
        this.description = description;
        this.price = price;
        this.images = images;
        this.entryDate = entryDate;
        this.brand = brand;
        this.category = category;
    }
}